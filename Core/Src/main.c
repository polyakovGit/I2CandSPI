		break;
	case 3:
		sym_to_indicator = 0b10110000;
		break;
	case 4:
		sym_to_indicator = 0b10011001;
		break;
	case 5:
		sym_to_indicator = 0b10010010;
		break;
	case 6:
		sym_to_indicator = 0b10000010;
		break;
	case 7:
		sym_to_indicator = 0b11111000;
